int mi[100][100];

inline void init() {
    /*Init Here*/
    mi[1][1] = mi[1][2] = 1;
    for (int i = 2; i <= 65; i++) {
        mi[i][1] = qpow(2, i - 1);
        for (int j = 2; j <= (i + 2) / 2; j++) {
            mi[i][j] = 2 * mi[i - 1][j];
            int x = i - 1, y = j - 1;
            while (x > 0 && y > 0) {
                mi[i][j] = mi[i][j] + mi[x][y];
                x--;
                y--;
            }
        }
        for (int j = (i + 1) / 2 + 1; j <= i + 1; j++) {
            mi[i][j] = mi[i][i + 1 - j + 1];
        }
    }
}

struct edge {
    int to, w;
};

struct node {
    int fa, depth, len;
    vector<edge> e;
} a[500005];

void dfs(int root, int fa = 0) {
    a[root].fa = fa;
    a[root].depth = a[fa].depth + 1;
    for (auto &i: a[root].e) {
        if (i.to != fa) {
            a[i.to].len = i.w;
            dfs(i.to, root);
        }
    }
}

int get(const deque<int> &d) {
    int ans = 0;
    if (d.size() == 1) {
        return d[0];
    }
    int n = d.size() - 1;
    for (int i = 0; i < d.size(); i++) {
        ans = ans + d[i] * mi[n][i + 1];
    }
    return ans;
}

