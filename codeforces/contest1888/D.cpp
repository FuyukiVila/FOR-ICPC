void genshin_start() {
    int n, m;
    cin >> n >> m;
    vector<int> a(n + 1), b(n + 1);
    multiset<int> c, d;
    a[1] = 1;
    for (int i = 2; i <= n; i++) {
        cin >> a[i];
        c.insert(a[i]);
    }
    for (int i = 1; i <= n; i++) {
        cin >> b[i];
        d.insert(b[i]);
    }
    sort(a.begin() + 1, a.end());
    sort(b.begin() + 1, b.end());
    auto check = [&](int k) {
        int del = n - k;
        for (int i = 1; i <= k; i++) {
            if (a[i] >= b[i + del]) {
                return false;
            }
        }
        return true;
    };
    int l = 0, r = n;
    while (l <= r) {
        int mid = (l + r) >> 1;
        if (check(mid)) {
            l = mid + 1;
        } else {
            r = mid - 1;
        }
    }
    ll ans = n - r;
    l = 1, r = m;
    while (l <= r) {
        int mid = (l + r) >> 1;
        bool flag = true;
        auto k = c.insert(mid);
        for (auto i = c.begin(), j = next(d.begin(), ans); j != d.end(); i++, j++) {
            if (*i >= *j) {
                flag = false;
                break;
            }
        }
        if (flag) {
            l = mid + 1;
        } else {
            r = mid - 1;
        }
        c.erase(k);
    }
    cout << ans * r + (ans + 1ll) * (ll) (m - r) << '\n';

}

