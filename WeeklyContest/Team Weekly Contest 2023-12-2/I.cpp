const int maxn = 109;
unordered_map<ll,map<ll,ll> > dp[maxn];
void solve(){
    int n;
    cin >> n;
    dp[0][0][0] = 0;
    int a,b,c;
    for(int i = 1;i <= n;i ++){
        cin >> a >> b >> c;
        for(auto [A,t]:dp[i - 1]){
            for(auto [D,r] : t ){
                dp[i][A + D][D] = max(dp[i][A + D][D],dp[i - 1][A][D] + A + D + a);
                dp[i][A + D + c][D] = max(dp[i][A + D + c][D],dp[i - 1][A][D]);
                dp[i][A + D][D + b] = max(dp[i][A + D][D + b],dp[i - 1][A][D]);
            }
        }
    }
    ll ans = 0;
    for(auto [A,t]:dp[n]){
        for(auto [D,r] : t){
            ans = max(ans,dp[n][A][D]);
        }
    }
    cout << ans << endl;
}
